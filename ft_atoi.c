#include "push_swap.h"

long	ft_atoi(char *str)
{
	int				i;
	int				a;
	unsigned long	nb;

	i = 0;
	a = 1;
	nb = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			a *= -1;
		i++;
	}
	while (nb < 2147483648 && str[i] >= '0' && str[i] <= '9')
		nb = (str[i++] - 48) + nb * 10;
	return (nb * a);
}
